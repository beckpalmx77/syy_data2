SELECT
 APCAT.APCAT_CODE,
 APCAT.APCAT_NAME,
 APFILE.AP_ENABLE,
 APFILE.AP_CODE,
 APFILE.AP_NAME,
 APFILE.AP_REMARK,
 APSUMMARY.APS_CRE_LIM,
 ACCOUNTCHART.AC_CODE,
 APFILE.AP_ACCESS,
 DEPTTAB.DEPT_CODE,
